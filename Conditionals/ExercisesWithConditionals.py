# == equal
# != not equal to
# >  greater than
# >= greater than or equal to
# <  less than
# <= less than or equal to

num = 30

if num < 50:
    if num >= 25:
        print("Greater than 25, less than 50!")
    elif num > 15:
        print("Positive number less than 25!")
    elif num > 0:
        print("Less than 25!")
else:
    print("Greater than 50!")

